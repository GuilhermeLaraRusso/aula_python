# Ordenação de lista

lista = [1, 10, 7, 5]
lista_animal = ['cachorro', 'gato', 'elefante', 'lobo', 'arara']

lista.sort()
lista_animal.sort()
print(lista)
print(lista_animal)

# .sort é uma função e deve ser feita antes de printar

lista_animal.reverse()
print(lista_animal)

# .reverse ordena em ordem reversa.